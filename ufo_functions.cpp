std::string BuildBlankAnswer(std::string codeword) {
    std::string result = "";
    for (int i = 0; i < codeword.size(); i++) {
        result += "_";
    }
    return result;
}

void EndGame(std::string codeword, std::string answer) {
    if (codeword == answer) {
        std::cout << "Hooray! You saved the person and earned a medal of honor!";
    } else {
        std::cout << "Oh no! The UFO just flew away with another person!";
    }
}

void SetColorRed() {
    HANDLE hConsole = GetStdHandle(STD_OUTPUT_HANDLE);
    SetConsoleTextAttribute(hConsole, FOREGROUND_RED);
}

void ResetColor() {
    HANDLE hConsole = GetStdHandle(STD_OUTPUT_HANDLE);
    SetConsoleTextAttribute(hConsole, FOREGROUND_BLUE | FOREGROUND_GREEN | FOREGROUND_RED);
}

std::string get_substring(std::string input, std::vector<int> indices) {
    std::string result;
    for (int i : indices) {
        result += input[i];
    }
    return result;
}

void display_status(std::string answer, std::string guess, std::vector<int> red_pos){
    std::cout << "Your guess: " << guess << "\n";
    std::cout << "Answer: ";
    for (int i = 0; i < answer.size(); i++) {
        if (std::find(red_pos.begin(), red_pos.end(), i) != red_pos.end()) {
            SetColorRed();
            std::cout << guess[i];
            ResetColor();
        } else {
            std::cout << answer[i];
        }
    }
    std::cout << "\n";
}

